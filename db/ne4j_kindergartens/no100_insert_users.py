from neo4j_connect import driver
import csv
from datetime import datetime


# ============================================================
# 날짜 문자열 정리 함수
# ============================================================
def fix_datetime(dt):
    if not dt or dt == "":
        return None

    value = str(dt).strip()

    # 잘못된 데이터 보정 (끝에 T 제거)
    if value.endswith("T"):
        value = value[:-1]

    try:
        value = value.replace(" ", "T", 1)
        return datetime.fromisoformat(value).isoformat()
    except Exception:
        return value


# ============================================================
# 문자열 정리 함수
# ============================================================
def clean_value(value):
    if value is None:
        return None

    value = str(value).strip()
    if value == "":
        return None

    return value


# ============================================================
# User 노드 생성/업데이트
# ============================================================
def create_user_node(tx, row):
    print("DEBUG USER ROW:", row)

    user_id = int(row["user_id"])

    tx.run("""
        // ======================================================
        // 1. User 노드 생성 (중복 방지)
        // ======================================================
        MERGE (u:User {user_id: $user_id})

        // ======================================================
        // 2. 속성 설정
        // ======================================================
        SET u.login_id = $login_id,
            u.email = $email,
            u.phone = $phone,
            u.password_hash = $password_hash,
            u.status = $status,
            u.last_login_at = $last_login_at,
            u.created_at = $created_at,
            u.updated_at = $updated_at
    """,
    user_id=user_id,
    login_id=clean_value(row.get("login_id")),
    email=clean_value(row.get("email")),
    phone=clean_value(row.get("phone")),
    password_hash=clean_value(row.get("password_hash")),
    status=clean_value(row.get("status")),
    last_login_at=fix_datetime(row.get("last_login_at")),
    created_at=fix_datetime(row.get("created_at")),
    updated_at=fix_datetime(row.get("updated_at"))
    )


# ============================================================
# 메인 실행
# ============================================================
if __name__ == "__main__":

    csv_file_path = "./data/100_users.csv"

    with open(csv_file_path, "r", encoding="utf-8-sig") as file:
        reader = csv.DictReader(file)

        with driver.session() as session:
            for row in reader:
                try:
                    if not row.get("user_id"):
                        print("⚠️ SKIP: user_id 없음", row)
                        continue

                    session.execute_write(create_user_node, row)

                    print(f"✅ User 적재 완료: user_id={row['user_id']}, login_id={row.get('login_id')}")

                except Exception as e:
                    print(f"❌ User 적재 실패: user_id={row.get('user_id')}, error={e}")

    print("🎉 users.csv → User 노드 적재 완료")


